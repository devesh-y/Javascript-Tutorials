console.log(Math.abs(-56.56));                     //The absolute value of number (that is, the number without any sign)
console.log(Math.ceil(5.5));                  //The smallest integer greater than or equal to number
console.log(Math.cos(0));                      //The cosine of number; returned values range from -1 to 1 radians
console.log(Math.exp(1));                // E raised to the power of number
console.log(Math.floor(5.5));                    //The largest integer that is less than or equal to number
console.log(Math.log(2.718281828459045));                       //The natural logarithm (base E) of number
console.log(Math.log2(2));
console.log(Math.max(92, 74));              //The larger of number1 and number2
console.log(Math.min(92, 74));                //The smaller of number1 and number2
console.log(Math.pow(9, 4));                //number1 raised to the power of number2
console.log(Math.random());                          //A random number between 0 and 1  , 1 not included
console.log(Math.round(56.5));                   //The integer closest to number
console.log(Math.sin(3.14));                       //The sine of number; returned values range from -1 to 1 radians
console.log(Math.sqrt(25) );                     //The square root of number (which must be greater than or equal to 0)
console.log(Math.tan(3.14));                      //The tangent of number, in radians